package mainPackage;

/*
 * THIS CLASS WAS CREATED TO SAVE TEXT WHILE CHANGING FRAME SETTINGS
 * SO THE USER DOES NOT HAVE TO WORRY ABOUT LOSING CONTENT 
 * WHILE CHENGING THE VIEW OF THE APPLICATION
 */

public class SaveText {

	String saveText;
	
	public void setSavedText(String st) {
		
		saveText = st;
	}
	
	public String getSavedText() {
		
		return saveText;
	}
}
